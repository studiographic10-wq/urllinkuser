function applyLanguage(lang) {
    currentLanguage = lang;

    // Update active state of new language buttons
    const langEnBtn = document.getElementById('lang-en');
    const langOrBtn = document.getElementById('lang-or');
    if (langEnBtn && langOrBtn) {
        if (lang === 'en') {
            langEnBtn.classList.add('active');
            langOrBtn.classList.remove('active');
        } else {
            langOrBtn.classList.add('active');
            langEnBtn.classList.remove('active');
        }
    }

    const t = translations[lang];

    // Update static elements with data-translate attributes
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (t[key]) {
            el.textContent = t[key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });

    // Update Font Family based on language
    if (lang === 'or') {
        document.body.style.fontFamily = "'Noto Sans Odia', sans-serif";
    } else {
        document.body.style.fontFamily = "'Inter', -apple-system, sans-serif";
    }

    // Reset Chat Greeting if it's the only message
    const chatMessages = document.getElementById('chat-messages');
    if (chatMessages && chatMessages.children.length === 1) {
        chatMessages.innerHTML = '';
        const greetingText = t['bot_intro'];
        const introDiv = document.createElement('div');
        introDiv.className = 'message bot-message';
        introDiv.innerHTML = greetingText;
        chatMessages.appendChild(introDiv);

        // Audio greeting removed as per user request
    }

    // Re-render services to update category titles
    renderServices(services);
}

function speakText(textWithHtml) {
    if (!('speechSynthesis' in window)) return;

    // Stop any current speech
    window.speechSynthesis.cancel();

    // Create a temporary element to strip HTML tags
    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = textWithHtml;
    const textToSpeak = tempDiv.textContent || tempDiv.innerText || "";

    if (!textToSpeak.trim()) return;

    const utterance = new SpeechSynthesisUtterance(textToSpeak);

    // Attempt to set language
    if (currentLanguage === 'or') {
        utterance.lang = 'or-IN'; // If supported
        // Fallback for Oriya might be Hindi if Oriya TTS isn't widely available
    } else {
        utterance.lang = 'en-IN'; // Indian English
    }

    utterance.rate = 1;
    utterance.pitch = 1;

    window.speechSynthesis.speak(utterance);
}

function renderServices(servicesToRender) {
    const mainGrid = document.getElementById('servicesGrid');
    mainGrid.innerHTML = '';
    const t = translations[currentLanguage];

    // Group by category
    const grouped = servicesToRender.reduce((acc, s) => {
        if (!acc[s.category]) acc[s.category] = [];
        acc[s.category].push(s);
        return acc;
    }, {});

    Object.keys(grouped).forEach(catName => {
        const container = document.createElement('div');
        container.className = `category-container ${categoryStyles[catName] || ''}`;

        // Translate category name if available, else keep original
        const displayCatName = t[catName] || catName;

        container.innerHTML = `
            <h3 class="category-title">${displayCatName}</h3>
            <div class="category-grid"></div>
        `;

        const catGrid = container.querySelector('.category-grid');

        grouped[catName].forEach(service => {
            const card = document.createElement('a');
            card.href = service.url;
            card.target = '_blank';
            card.className = 'service-item';

            // Determine Name based on Language
            let displayName = service.name;
            if (currentLanguage === 'or' && service.name_or) {
                displayName = service.name_or;
            }

            card.innerHTML = `
                <div class="icon-wrapper">
                    <img src="${service.icon}" alt="${displayName}" class="service-icon" onerror="this.src='https://img.icons8.com/color/512/external-link.png'">
                </div>
                <div class="service-content">
                    <span class="service-name">${displayName}</span>
                </div>
            `;
            catGrid.appendChild(card);
        });

        mainGrid.appendChild(container);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    // Initialize Language
    // Initialize Language Buttons
    const langEnBtn = document.getElementById('lang-en');
    const langOrBtn = document.getElementById('lang-or');

    if (langEnBtn && langOrBtn) {
        langEnBtn.addEventListener('click', () => applyLanguage('en'));
        langOrBtn.addEventListener('click', () => applyLanguage('or'));
    }

    // Font Size Adjustment Logic
    const fontDecBtn = document.getElementById('font-dec');
    const fontNormBtn = document.getElementById('font-norm');
    const fontIncBtn = document.getElementById('font-inc');
    let currentFontSize = 100; // Percentage

    function setFontSize(size) {
        currentFontSize = size;
        document.documentElement.style.fontSize = `${currentFontSize}%`;

        // Update active class
        [fontDecBtn, fontNormBtn, fontIncBtn].forEach(btn => btn?.classList.remove('active'));
        if (size < 100 && fontDecBtn) fontDecBtn.classList.add('active');
        else if (size > 100 && fontIncBtn) fontIncBtn.classList.add('active');
        else if (size === 100 && fontNormBtn) fontNormBtn.classList.add('active');
    }

    if (fontDecBtn && fontNormBtn && fontIncBtn) {
        fontDecBtn.addEventListener('click', () => {
            if (currentFontSize > 70) setFontSize(currentFontSize - 10);
        });
        fontNormBtn.addEventListener('click', () => {
            setFontSize(100);
        });
        fontIncBtn.addEventListener('click', () => {
            if (currentFontSize < 150) setFontSize(currentFontSize + 10);
        });
    }

    // Initial Render
    applyLanguage(currentLanguage);

    const searchInput = document.getElementById('searchInput');
    searchInput.addEventListener('input', (e) => {
        const term = e.target.value.toLowerCase();
        const filtered = services.filter(s => s.name.toLowerCase().includes(term));
        renderServices(filtered);
    });

    // Banner Slider Logic
    const slides = document.querySelectorAll('.slide');
    const dots = document.querySelectorAll('.dot');
    let currentSlide = 0;
    const slideInterval = 5000; // 5 seconds

    function showSlide(index) {
        if (slides.length === 0) return;
        slides.forEach(slide => slide.classList.remove('active'));
        dots.forEach(dot => dot.classList.remove('active'));

        slides[index].classList.add('active');
        dots[index].classList.add('active');
    }

    function nextSlide() {
        if (slides.length === 0) return;
        currentSlide = (currentSlide + 1) % slides.length;
        showSlide(currentSlide);
    }

    // Auto Advance
    setInterval(nextSlide, slideInterval);

    // Click on dots
    dots.forEach((dot, index) => {
        dot.addEventListener('click', () => {
            currentSlide = index;
            showSlide(currentSlide);
        });
    });

    // Home Button Scroll to Services
    const homeLink = document.getElementById('homeLink');
    if (homeLink) {
        homeLink.addEventListener('click', (e) => {
            e.preventDefault();
            const heroSection = document.querySelector('.hero');
            if (heroSection) {
                heroSection.scrollIntoView({ behavior: 'smooth' });
            }
        });
    }

});
